const byId = Object.fromEntries(BANGLADESH_DISTRICTS.map((d) => [d.id, d]));

export function getDistrictById(id) {
  return id ? byId[id] || null : null;
}

export function formatDistrictLabel(id, lang = "en") {
  const d = getDistrictById(id);
  if (!d) return "";
  return lang === "bn" ? d.nameBn : d.nameEn;
}
